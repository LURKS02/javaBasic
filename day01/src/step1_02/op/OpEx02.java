package step1_02.op;

public class OpEx02 {

	public static void main(String[] args) {
		//덧셈 연산자
		System.out.println(10 + 3);
				
		//연결 연산자
				
		// 문자 + 문자 = 문자
		// 숫자 + 문자 = 문자
				
		System.out.println("안녕" + "하세요");
		System.out.println("10" + 3);
		System.out.println(3 + "10");
				
		System.out.println("더하기 = " + 10 + 3);
		System.out.println("더하기 = " + (10 + 3));

	}

}
