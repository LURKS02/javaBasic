package step1_02.op;

public class OpEx01 {

	public static void main(String[] args) {
		//더하기
		System.out.println(10 + 3);
		//빼기
		System.out.println(10 - 3);
		//곱하기
		System.out.println(10 * 3);
		//나누기
				
		// 정수 / 정수 = 정수
		// 정수 / 실수 = 실수
				
		System.out.println(10 / 3);
		System.out.println(10 / 3.0);
		System.out.println(10 % 3);
				
	}

}
