package step1_04.op;

//비교 연산자
// >, <, ==, !=, >=, <=

public class OpEx05 {

	public static void main(String[] args) {
		System.out.println(10 == 3);
		System.out.println(3 != 8);
		System.out.println(5 > 38);
		System.out.println(12 > 3);
		System.out.println(11 < 11);
		System.out.println(3 <= 10);
	}

}
